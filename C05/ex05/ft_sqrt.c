int	ft_sqrt(int nb)
{
	int a;

	a = 0;
	if (nb == 0 || nb <= 2147395600)
	{
		while ((a * a) <= nb)
		{
			if ((a * a) == nb)
				return (a);
			else
				a++;
		}
	}
	return (0);
}
